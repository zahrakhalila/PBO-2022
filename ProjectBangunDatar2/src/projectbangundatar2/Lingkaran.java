public class Lingkaran {
    // LUAS //
    double luas(int r) {
        return (3.14 * r * r);
    }
    double luas(double r) {
        return (3.14 * r * r);
    }
    
    // KELILING //
    double keliling(int r) {
        return (2 * 3.14 * r);
    }
    double keliling(double r) {
        return (2 * 3.14 * r);
    }
}
